export function getDictionary(locale: Locale) {
  return translations[locale]
}

export function formatCurrencyILS(value: number, locale: Locale) {
  return new Intl.NumberFormat(getCurrencyLocale(locale), {
    style: 'currency',
    currency: 'ILS',
    maximumFractionDigits: 0,
  }).format(value)
}

export function formatDateLocalized(value: string | Date, locale: Locale) {
  return new Intl.DateTimeFormat(getCurrencyLocale(locale), {
    year: 'numeric',
    month: 'long',
    day: 'numeric',
  }).format(new Date(value))
}

export function translate(template: string, replacements: Record<string, string | number>) {
  return Object.entries(replacements).reduce(
    (result, [key, replacement]) => result.replaceAll(`{${key}}`, String(replacement)),
    template,
  )
}
